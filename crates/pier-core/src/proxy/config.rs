/// One row in the multi-target dynamic config: a domain that should route
/// to a specific upstream URL, optionally tagged with the compose-service
/// it represents (used to group routers + Traefik service blocks).
#[derive(Debug, Clone)]
pub struct DomainTarget {
    pub domain: String,
    pub use_tls: bool,
    /// `Some("svc")` for multi-service composes, `None` for single-target services.
    pub compose_service: Option<String>,
    /// Full upstream URL, e.g. `http://api:3000`.
    pub target_url: String,
}

/// Generate a Traefik dynamic config that supports multiple HTTP routers
/// fanning out to different upstream services. Used when one Pier resource
/// (one compose file) hosts several containers that each need their own
/// domain (e.g. `tma.x → telegram-bot:3000` + `max.x → max-bot:3000`).
///
/// Routers are grouped by `compose_service` (None = single shared service).
/// Within a group every domain points at the same Traefik service block.
fn generate_dynamic_config_multi_target(service_id: &str, targets: &[DomainTarget]) -> String {
    use std::collections::BTreeMap;

    // Stable group key: None comes first, then sorted compose_service names.
    let mut groups: BTreeMap<String, Vec<&DomainTarget>> = BTreeMap::new();
    for t in targets {
        let key = t.compose_service.clone().unwrap_or_default();
        groups.entry(key).or_default().push(t);
    }

    let mut routers = String::new();
    let mut middlewares = String::new();
    let mut services_yaml = String::new();
    let mut router_idx = 0usize;
    for (group_key, group) in &groups {
        let svc_suffix = if group_key.is_empty() {
            String::new()
        } else {
            format!("-{}", sanitize_traefik_name(group_key))
        };
        let svc_name = format!("pier-{service_id}{svc_suffix}");
        // Upstream URL is identical within a compose-service group; take the first.
        let upstream = group
            .first()
            .map(|t| t.target_url.as_str())
            .unwrap_or_default();
        services_yaml.push_str(&format!(
            r#"    {svc_name}:
      loadBalancer:
        servers:
          - url: "{upstream}"
"#
        ));

        for t in group {
            let router_name = if router_idx == 0 {
                format!("pier-{service_id}{svc_suffix}")
            } else {
                format!("pier-{service_id}{svc_suffix}-{router_idx}")
            };
            router_idx += 1;
            let tls_section = if t.use_tls {
                "\n      tls:\n        certResolver: letsencrypt".to_string()
            } else {
                String::new()
            };
            let (hostname, path_prefix) = if let Some(slash) = t.domain.find('/') {
                (&t.domain[..slash], Some(&t.domain[slash..]))
            } else {
                (t.domain.as_str(), None)
            };
            let (rule, middleware_ref) = if let Some(path) = path_prefix {
                let mw_name = format!("strip-{router_name}");
                middlewares.push_str(&format!(
                    "    {mw_name}:\n      stripPrefix:\n        prefixes:\n          - \"{path}\"\n"
                ));
                (
                    format!("Host(`{hostname}`) && PathPrefix(`{path}`)"),
                    format!("\n      middlewares:\n        - {mw_name}"),
                )
            } else {
                (format!("Host(`{hostname}`)"), String::new())
            };
            routers.push_str(&format!(
                r#"    {router_name}:
      rule: "{rule}"
      entryPoints:
        - websecure{tls_section}{middleware_ref}
      service: {svc_name}
"#
            ));
        }
    }

    let mw_section = if middlewares.is_empty() {
        String::new()
    } else {
        format!("  middlewares:\n{middlewares}")
    };

    format!(
        r#"# Auto-generated by Pier for service {service_id}
http:
  routers:
{routers}{mw_section}  services:
{services_yaml}"#
    )
}

/// Sanitize a compose-service name for use in Traefik router/service IDs.
/// Traefik tolerates most characters but we restrict to `[A-Za-z0-9_-]`
/// to be safe. Replaces anything else with `-`.
fn sanitize_traefik_name(s: &str) -> String {
    s.chars()
        .map(|c| {
            if c.is_ascii_alphanumeric() || c == '-' || c == '_' {
                c
            } else {
                '-'
            }
        })
        .collect()
}

/// Regenerate the Traefik dynamic config from a list of (domain → target_url)
/// pairs grouped by compose-service. This is the multi-target variant used
/// when one Pier resource contains multiple containers each needing their
/// own domain. For single-target services callers can keep using
/// `regenerate_service_config` (it's a one-target wrapper that delegates here).
pub fn regenerate_service_config_multi(
    data_dir: &Path,
    service_id: &str,
    targets: &[DomainTarget],
) -> Result<()> {
    let dynamic_dir = data_dir.join("traefik").join("dynamic");
    std::fs::create_dir_all(&dynamic_dir)?;
    let file_path = dynamic_dir.join(format!("{service_id}.yml"));

    if targets.is_empty() {
        if file_path.exists() {
            std::fs::remove_file(file_path)?;
        }
        return Ok(());
    }

    let config = generate_dynamic_config_multi_target(service_id, targets);
    if let Ok(existing) = std::fs::read_to_string(&file_path) {
        if existing == config {
            return Ok(());
        }
    }
    std::fs::write(file_path, config)?;
    Ok(())
}

